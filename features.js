// ── F10: Keyboard Shortcuts Panel ────────────────────────────
const SHORTCUTS = [
  { keys: ['Ctrl','Enter'],  desc: 'Run Diagnosis' },
  { keys: ['Ctrl','K'],      desc: 'Focus log input' },
  { keys: ['Ctrl','/'],      desc: 'Show shortcuts' },
  { keys: ['Ctrl','E'],      desc: 'Export report' },
  { keys: ['Ctrl','S'],      desc: 'Share link' },
  { keys: ['Ctrl','H'],      desc: 'Go to History' },
  { keys: ['Ctrl','D'],      desc: 'Go to Diagnose' },
  { keys: ['Escape'],        desc: 'Close modals' },
];

function showShortcuts() {
  document.getElementById('shortcuts-modal').classList.add('open');
}
function hideShortcuts() {
  document.getElementById('shortcuts-modal').classList.remove('open');
}

document.getElementById('shortcuts-modal-close').addEventListener('click', hideShortcuts);
document.getElementById('shortcuts-btn').addEventListener('click', showShortcuts);

document.getElementById('shortcuts-list').innerHTML = SHORTCUTS.map(s =>
  `<div class="shortcut-row">
    <div class="shortcut-keys">${s.keys.map(k=>`<kbd>${k}</kbd>`).join('+')}</div>
    <div class="shortcut-desc">${s.desc}</div>
  </div>`
).join('');

// Global keyboard shortcuts
document.addEventListener('keydown', e => {
  const ctrl = e.ctrlKey || e.metaKey;
  if (ctrl && e.key === '/') { e.preventDefault(); showShortcuts(); }
  if (ctrl && e.key === 'k') { e.preventDefault(); document.getElementById('log-input').focus(); window.switchSection('diagnose'); }
  if (ctrl && e.key === 'e') { e.preventDefault(); exportMarkdown(); }
  if (ctrl && e.key === 's') { e.preventDefault(); generateShareLink(); }
  if (ctrl && e.key === 'h') { e.preventDefault(); window.switchSection('history'); }
  if (ctrl && e.key === 'd') { e.preventDefault(); window.switchSection('diagnose'); }
  if (e.key === 'Escape') {
    document.querySelectorAll('.modal-overlay.open, .feature-modal.open').forEach(m => m.classList.remove('open'));
  }
});

// ── Close modals on overlay click ────────────────────────────
document.querySelectorAll('.feature-modal').forEach(modal => {
  modal.addEventListener('click', e => {
    if (e.target === modal) modal.classList.remove('open');
  });
});
