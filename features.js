function showCompare(prev, curr) {
  const modal = document.getElementById('compare-modal');
  const body  = document.getElementById('compare-body');
  const fields = ['stage','category','confidence','fixTime','rootCause'];
  body.innerHTML = fields.map(f => {
    const pv = (prev[f]||'—'), cv = (curr[f]||'—');
    const changed = pv !== cv;
    return `<div class="compare-row ${changed?'changed':''}">
      <div class="compare-field">${f}</div>
      <div class="compare-val old">${escHtml(String(pv))}</div>
      <div class="compare-arrow">→</div>
      <div class="compare-val new">${escHtml(String(cv))}</div>
    </div>`;
  }).join('');
  modal.classList.add('open');
}

document.getElementById('rerun-btn').addEventListener('click', async () => {
  if (!currentDiagnosis) return toast('Run a diagnosis first', 'error');
  saveLastDiagnosis(currentDiagnosis);
  await window.runDiagnosis();
  if (_lastDiagnosis) showCompare(_lastDiagnosis, currentDiagnosis);
});
document.getElementById('compare-modal-close').addEventListener('click', () => document.getElementById('compare-modal').classList.remove('open'));

