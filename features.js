// ── F1: Export Report (PDF / Markdown) ──────────────────────
function exportMarkdown() {
  if (!currentDiagnosis) return toast('Run a diagnosis first', 'error');
  const d = currentDiagnosis;
  const md = `# DeployIQ Diagnosis Report\n\n**Failed Stage:** ${d.stage}\n**Category:** ${d.category}\n**Confidence:** ${d.confidence}\n**Fix Time:** ${d.fixTime}\n**Platform:** ${d.platform || '—'}\n\n## Root Cause\n${d.rootCause}\n\n## Why It Broke\n${(d.technical||'').replace(/<[^>]+>/g,'')}\n\n## Fix Steps\n${(d.steps||[]).map((s,i)=>`${i+1}. **${s.label}**\n\`\`\`\n${s.cmd}\n\`\`\``).join('\n\n')}\n\n## Prevention\n${(d.prevention||[]).map(p=>`- ${p}`).join('\n')}\n\n---\n*Generated by DeployIQ ${new Date().toLocaleString()}*`;
  const blob = new Blob([md], {type:'text/markdown'});
  const url  = URL.createObjectURL(blob);
  const a    = Object.assign(document.createElement('a'), {href:url, download:'deployiq-report.md'});
  a.click(); URL.revokeObjectURL(url);
  toast('Markdown report downloaded ✓', 'success');
}

function exportPDF() {
  if (!currentDiagnosis) return toast('Run a diagnosis first', 'error');
  window.print();
  toast('Use browser Print → Save as PDF', 'success');
}

document.getElementById('export-md-btn').addEventListener('click', exportMarkdown);
document.getElementById('export-pdf-btn').addEventListener('click', exportPDF);

// ── F2: Shareable Link ───────────────────────────────────────
function generateShareLink() {
  if (!currentDiagnosis) return toast('Run a diagnosis first', 'error');
  const d = currentDiagnosis;
  const payload = btoa(unescape(encodeURIComponent(JSON.stringify({
    stage: d.stage, category: d.category, confidence: d.confidence,
    fixTime: d.fixTime, platform: d.platform, rootCause: d.rootCause,
    prevention: d.prevention, ts: new Date().toISOString()
  }))));
  const url = `${location.origin}${location.pathname}?share=${payload}`;
  navigator.clipboard.writeText(url).then(() => toast('Share link copied to clipboard ✓', 'success'));
  document.getElementById('share-link-input').value = url;
  document.getElementById('share-modal').classList.add('open');
}

function loadSharedDiagnosis() {
  const params = new URLSearchParams(location.search);
  const s = params.get('share');
  if (!s) return;
  try {
    const d = JSON.parse(decodeURIComponent(escape(atob(s))));
    currentDiagnosis = d;
    renderOutput(d);
    document.getElementById('output-section').style.display = '';
    toast('Shared diagnosis loaded ✓', 'success');
  } catch(e) {}
}

document.getElementById('share-btn').addEventListener('click', generateShareLink);
document.getElementById('share-modal-close').addEventListener('click', () => document.getElementById('share-modal').classList.remove('open'));
document.getElementById('share-copy-btn').addEventListener('click', () => {
  navigator.clipboard.writeText(document.getElementById('share-link-input').value);
  toast('Copied!', 'success');
});
loadSharedDiagnosis();

