// ── F6: Tag & Organize History ────────────────────────────────
function addTagToEntry(id, tag) {
  const entry = history.find(e => e.id === id);
  if (!entry) return;
  entry.tags = entry.tags || [];
  if (!entry.tags.includes(tag)) entry.tags.push(tag);
  localStorage.setItem('deployiq_history', JSON.stringify(history));
  renderHistory();
  toast(`Tag "${tag}" added`, 'success');
}

function removeTagFromEntry(id, tag) {
  const entry = history.find(e => e.id === id);
  if (!entry || !entry.tags) return;
  entry.tags = entry.tags.filter(t => t !== tag);
  localStorage.setItem('deployiq_history', JSON.stringify(history));
  renderHistory();
}

// Patch renderHistory to show tags and add-tag button
const _origRenderHistory = renderHistory;
window.renderHistoryWithTags = function() {
  _origRenderHistory();
  document.querySelectorAll('.history-item').forEach(item => {
    const id = parseInt(item.dataset.id);
    const entry = history.find(e => e.id === id);
    if (!entry) return;
    const tags = entry.tags || [];
    let tagRow = item.querySelector('.history-tags');
    if (!tagRow) {
      tagRow = document.createElement('div');
      tagRow.className = 'history-tags';
      item.querySelector('.history-info').appendChild(tagRow);
    }
    tagRow.innerHTML = tags.map(t =>
      `<span class="htag" data-id="${id}" data-tag="${escHtml(t)}">${escHtml(t)} <span class="htag-remove">×</span></span>`
    ).join('') +
      `<button class="htag-add" data-id="${id}">+ tag</button>`;
  });

  document.querySelectorAll('.htag-remove').forEach(btn => {
    btn.addEventListener('click', e => {
      e.stopPropagation();
      const span = btn.closest('.htag');
      removeTagFromEntry(parseInt(span.dataset.id), span.dataset.tag);
    });
  });

  document.querySelectorAll('.htag-add').forEach(btn => {
    btn.addEventListener('click', e => {
      e.stopPropagation();
      const id = parseInt(btn.dataset.id);
      const tag = prompt('Enter tag name:');
      if (tag && tag.trim()) addTagToEntry(id, tag.trim());
    });
  });
};

